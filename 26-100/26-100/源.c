//在海军节开幕式上，有A、B、C三艘军舰要同时开始鸣放礼炮各21响。
//已知A舰每隔5秒放一次，B舰每隔6秒放一次, C舰每隔7秒放一次，假设各炮手对时间的掌握非常准确。
//请编程计算观众总共可以听到几次礼炮声。
#include<stdio.h>
int main()
{
	int A = 0, B = 0, C = 0, sum = 2;
	while (C <= 147)
	{
		C += 7;
		if (B <= 126)
		{
			B += 6;
			if (A <= 105)
			{
				A += 5;
				if (C % 6 == 0 || C % 5 == 0)
					sum++;
				if (C % 6 == 0 && C % 5 == 0)
					sum++;
			}
		}
	}
	printf("%d", 63 - sum);//54
	return 0;
}